<template>
    <div>
        <swiper>

            <swiper-item v-for = "item in banners">
                <a :href="item.link">
                    <img :src="item.image" :alt="item.title" @load="imageLoad">
                </a>
            </swiper-item>

        </swiper>
    </div>
</template>
<script>
import {Swiper, SwiperItem} from '@/components/commom/swiper/index'

export default {
    name: 'HomeSwiper',
    data() {
        return {
            isLoad: false
        }
    },
    components: {
        Swiper,
        SwiperItem
    },
    props: {
        banners: {
            type: Array,
            default() {
                return []
            }
        }
    },
    methods:{
        imageLoad() {
        if(!this.isLoad)
            {
                this.$emit("swiperImageLoad")
                this.isLoad = true
            }
        }
    }
}
</script>